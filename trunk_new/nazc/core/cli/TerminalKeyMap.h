#ifndef __TERMINAL_KEY_MAP_H__
#define __TERMINAL_KEY_MAP_H__

#define MAX_VIRTUAL_KEY_SIZE		10				// Virtual Key String Size

#define KEY_BS						0x08			// Backspace
#define KEY_TAB						0x09			// Tab
#define KEY_CR						0x0D			// CR
#define KEY_LF						0x0A			// LF
#define KEY_ESC						0x1B			// ESC
#define KEY_DEL						0x7F			// DEL

#define KEY_CTRL_C					0x03			// Ctrl+C
#define KEY_CTRL_D					0x04			// Ctrl+D

#define VKEY_UP						10				// UP Arrow
#define VKEY_DOWN					11				// Down Arrow
#define VKEY_RIGHT					12				// Right Arrow
#define VKEY_LEFT					13				// Left Arrow
#define VKEY_HOME					14				// Home
#define VKEY_END					15				// End
#define VKEY_PGUP					16				// Page Up
#define VKEY_PGDN					17				// Page Down

#define VKEY_INS					18				// Insert
#define VKEY_BREAK					19				// Pause/Break

#define VKEY_F1						100				// F1
#define VKEY_F2						101				// F2
#define VKEY_F3						102				// F3
#define VKEY_F4						103				// F4
#define VKEY_F5						104				// F5
#define VKEY_F6						105				// F6
#define VKEY_F7						106				// F7
#define VKEY_F8						107				// F8
#define VKEY_F9						108				// F9
#define VKEY_F10					109				// F10
#define VKEY_F11					110				// F11
#define VKEY_F12					111				// F12

#define VKEY_USERMENU				-2				// User Menu Key
#define VKEY_ATTP					-3				// ATTP Key

typedef struct	_tagCLIKEYMAP
{	
		const char	*pszKeyString;					// Key String
		int			nVKey;							// Virtual Key Value
}	CLIKEYMAP, *PCLIKEYMAP;

#endif	// __TERMINAL_KEY_MAP_H__
